    renderUpcomingDueDates() {
        const upcoming = Array.from(this.returns.values()).filter(r => r.filingStatus !== 'filed' && r.dueDate).sort((a, b) => a.dueDate - b.dueDate).slice(0, 5);
        if (upcoming.length === 0) return '<p>No upcoming due dates</p>';
        return upcoming.map(ret => `
            <div class="due-date-item ${ret.isOverdue ? 'overdue' : ''}">
                <span><strong>${ret.returnTypeInfo?.label}</strong> - ${ret.formattedPeriod}</span>
                <span>Due: ${ret.formattedDueDate}</span>
                <span class="status-badge" style="background:${ret.filingStatusInfo.color}20;color:${ret.filingStatusInfo.color}">${ret.filingStatusInfo.label}</span>
            </div>
        `).join('');
    }

