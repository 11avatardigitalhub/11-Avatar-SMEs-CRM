            await this.loadReturns();
            await this.loadITCLedger();
            this.setupEventListeners();
            this.calculateMetrics();

            if (document.getElementById('gst-container')) await this.render();
            console.log('[GST] Initialized successfully');
            EventBus.emit('gst:ready', { metrics: this.metrics });
