    calculateGST(invoiceData) {
        try {
            const { amount, gstRate, isInterState, placeOfSupply, billFromState } = invoiceData;
            const ratePercent = parseFloat(gstRate) || 18;
            const taxableAmount = parseFloat(amount) || 0;
            const gstAmount = taxableAmount * (ratePercent / 100);
            let cgst = 0, sgst = 0, igst = 0, utgst = 0, cess = 0;

            if (isInterState) {
                igst = gstAmount;
            } else {
                cgst = gstAmount / 2;
                sgst = gstAmount / 2;
                const isUT = ['32', '33', '34', '35', '36'].includes(placeOfSupply?.substring(0, 2));
                if (isUT) { utgst = sgst; sgst = 0; }
            }

            const totalGST = cgst + sgst + igst + utgst + cess;
            const totalAmount = taxableAmount + totalGST;

            return {
                taxableAmount: Math.round(taxableAmount * 100) / 100,
                gstRate: ratePercent,
                cgst: Math.round(cgst * 100) / 100,
                sgst: Math.round(sgst * 100) / 100,
                igst: Math.round(igst * 100) / 100,
                utgst: Math.round(utgst * 100) / 100,
                cess: Math.round(cess * 100) / 100,
                totalGST: Math.round(totalGST * 100) / 100,
                totalAmount: Math.round(totalAmount * 100) / 100,
                isInterState,
                hsnCode: invoiceData.hsnCode || null,
                sacCode: invoiceData.sacCode || null,
                placeOfSupply,
                billFromState
            };
        } catch (error) {
            console.error('[GST] Tax calculation failed:', error);
            return null;
        }
    }

    async generateEInvoice(invoiceId) {
        try {
            const invoice = await API.get(`/api/invoices/${invoiceId}`);
            if (!invoice.success) throw new Error('Invoice not found');
            if (!this.shouldGenerateEInvoice(invoice.data)) throw new Error('Invoice does not meet E-Invoice criteria');

            const gstCalculation = this.calculateGST({
                amount: invoice.data.subtotal,
                gstRate: invoice.data.gstRate || 18,
                isInterState: invoice.data.gstDetails?.isInterState || false,
                placeOfSupply: invoice.data.placeOfSupply,
                billFromState: this.gstinInfo?.stateCode
            });

            const einvoicePayload = this.buildEInvoicePayload(invoice.data, gstCalculation);
            const irpToken = await this.authenticateIRP();
            const response = await fetch(`${this.eInvoiceConfig.irpEndpoint}/invoice`, {
                method: 'POST',
                headers: { 'Authorization': `Bearer ${irpToken}`, 'Content-Type': 'application/json' },
                body: JSON.stringify(einvoicePayload)
            });

            if (!response.ok) throw new Error(`IRP submission failed: ${response.status}`);
            const irpResponse = await response.json();
            const einvoiceData = {
                id: `einv-${invoiceId}`,
                invoiceId: invoiceId,
                irn: irpResponse.Irn,
                ackNo: irpResponse.AckNo,
                ackDate: irpResponse.AckDt,
                qrCode: irpResponse.SignedQRCode,
                status: 'generated',
                generatedAt: new Date().toISOString()
            };

            await API.post(`${this.apiEndpoint}/e-invoices`, einvoiceData);
            this.eInvoices.set(einvoiceData.id, { ...einvoiceData, formattedDate: Formatters.date(einvoiceData.generatedAt), statusInfo: this.eInvoiceStatuses.generated, hasIRN: true, hasQRCode: true });
            this.metrics.totalEInvoices++;

            Toast.show('E-Invoice generated successfully', 'success');
            EventBus.emit('gst:einvoice-generated', einvoiceData);
            return einvoiceData;
        } catch (error) {
            console.error('[GST] E-Invoice generation failed:', error);
            Toast.show('E-Invoice generation failed: ' + error.message, 'error');
            return null;
        }
    }

    shouldGenerateEInvoice(invoice) {
        if (!this.eInvoiceConfig.enabled) return false;
        if (invoice.invoiceType === 'B2C') return false;
        if (invoice.totalAmount < 50000) return false;
        if (invoice.eInvoiceStatus === 'generated') return false;
        return true;
    }

